public class Character_pattern02
{
    public static void main(String[]args)
    {
        char i,j;
        for (i='A'; i<= 'E';i++)
        {
            for (j='A'; j<='E'; j++)
            {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
