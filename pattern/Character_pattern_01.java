public class Character_pattern01
{
    public static void main(String[]args)
    {
        char i,j;
        for (i='A'; i<= 'E'; i++)
        {
            for(j='A'; j<= 'E'; j++)
            {
                System.out.print(i);
            }
            System.out.println();
        }
    }
}
//output
//AAAAA
//BBBBB
//CCCCC
//DDDDD
//EEEEE
